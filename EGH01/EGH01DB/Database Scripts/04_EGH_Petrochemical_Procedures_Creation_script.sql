-- Добавление типа нефтепродукта
create procedure EGH.CreatePetrochemicalType(
						@КодТипаНефтепродукта int,  
						@НаименованиеТипаНефтепродукта nvarchar(max),
						@ТемператураКипения real,
						@Плотность real,
						@КинематическаяВязкость real,
						@Растворимость real,
						@КоэфНатяжения real,
						@ДинамическаяВязкость real,
						@КоэфДиффузии real,
						@КодКатегорииНефтепродукта int)
as begin 
declare @rc int  = @КодТипаНефтепродукта;
	begin try
		insert into dbo.ТипНефтепродукта(
							КодТипаНефтепродукта,
							НаименованиеТипаНефтепродукта,
							ТемператураКипения,
							Плотность,
							КинематическаяВязкость,
							Растворимость,
							КоэфНатяжения,
							ДинамическаяВязкость,
							КоэфДиффузии,
							КодКатегорииНефтепродукта) 
				values(@КодТипаНефтепродукта,
					   @НаименованиеТипаНефтепродукта,
					   @ТемператураКипения,
					   @Плотность,
					   @КинематическаяВязкость,
					   @Растворимость,
					   @КоэфНатяжения,
					   @ДинамическаяВязкость,
					   @КоэфДиффузии,
					   @КодКатегорииНефтепродукта); 
	end try
	begin catch
	    set @rc = -1;
	end catch 
  return @rc;  
end;
go

-- Удаление типа нефтепродукта
create procedure EGH.DeletePetrochemicalType (@КодТипаНефтепродукта int)
as begin 
    declare @rc int  = @КодТипаНефтепродукта;
    begin try 
	 delete dbo.ТипНефтепродукта where КодТипаНефтепродукта = @КодТипаНефтепродукта;
	end try
	begin catch
	    set @rc = -1;
	end catch   
	return @rc;
end; 
go

-- Получение типа нефтепродукта по ID
create  procedure EGH.GetPetrochemicalTypeByCode(@КодТипаНефтепродукта int) 
as begin 
    declare @rc int = -1;
	select  НаименованиеТипаНефтепродукта,
			ТемператураКипения,
			Плотность,
			КинематическаяВязкость,
			Растворимость,
			КоэфНатяжения,
			ДинамическаяВязкость,
			КоэфДиффузии,
			T.КодКатегорииНефтепродукта КодКатегорииНефтепродукта,
			НаименованиеКатегорииНефтепродукта
	from dbo.ТипНефтепродукта T inner join dbo.Категория_Нефтепродукта K 
	on T.КодКатегорииНефтепродукта = K.КодКатегорииНефтепродукта
	where КодТипаНефтепродукта = @КодТипаНефтепродукта;  
	set @rc = @@ROWCOUNT;
	return @rc;    
end;
go

-- Получение списка типов нефтепродукта
