 as begin
	declare @rc int = -1;
	select	КодТипаНефтепродукта,
			НаименованиеТипаНефтепродукта,
			ТемператураКипения,
			Плотность,
			КинематическаяВязкость,
			Растворимость,
			КоэфНатяжения,
			ДинамическаяВязкость,
			КоэфДиффузии,
			T.КодКатегорииНефтепродукта КодКатегорииНефтепродукта,
			НаименованиеКатегорииНефтепродукта
	from dbo.ТипНефтепродукта T inner join dbo.Категория_Нефтепродукта K 
	on T.КодКатегорииНефтепродукта = K.КодКатегорииНефтепродукта;
	set @rc = @@ROWCOUNT;
	return @rc;    
end;
go

-- Получение следующего значения типа нефтепродукта
create procedure EGH.GetNextPetrochemicalTypeCode(@КодТипаНефтепродукта int output)
 as begin
	declare @rc int = -1;
	set @КодТипаНефтепродукта = (select	max(КодТипаНефтепродукта)from dbo.ТипНефтепродукта) + 1;
	set @rc = @@ROWCOUNT;
	if @КодТипаНефтепродукта is null 
	begin
		set @КодТипаНефтепродукта = 1;
		set @rc = 1;
	end;
	return @rc;    
end;
go
